    private void jLabel10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel10MouseClicked
        // TODO add your handling code here:
        JFileChooser filechooser=new JFileChooser();
        int i=filechooser.showOpenDialog(rootPane);
        
        if(i==0)
        {
            file=filechooser.getSelectedFile();
            String img_path=file.getAbsolutePath();
            
            BufferedImage bufimg = null;
            Image dimg = null;
            try 
            {
                bufimg = ImageIO.read(new File(img_path));
                dimg = bufimg.getScaledInstance(jLabel6.getWidth(), jLabel6.getHeight(),Image.SCALE_SMOOTH);
            } 
            catch (IOException e)
            {
                e.printStackTrace();
            }
            
            jLabel6.setIcon(new ImageIcon(dimg));  //     /images/default-item.png
        }
    }//GEN-LAST:event_jLabel10MouseClicked

